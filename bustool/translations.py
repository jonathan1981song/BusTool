def translate_stop(stop_name: str) -> str:
    """Return a Chinese translation hint for a stop name, or '' if unknown."""
    lower = stop_name.lower()
    for keyword, chinese in _KEYWORDS:
        if keyword.lower() in lower:
            return chinese
    return ""
